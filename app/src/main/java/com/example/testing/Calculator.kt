package com.example.testing

class Calculator {

    fun computeAddition(a: Int, b:Int): Int {
       return a + b
    }
    fun computedDivision(a: Int, b:Int): Int{
        return a/b

    }

    fun computedSubtraction(a: Int, b:Int): Int{

        return a - b
    }
    fun computedMultiplication(a: Int, b:Int): Int{

        return a * b
    }
}